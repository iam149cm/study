/** Naming Conventions
 * - 대표적인 케이스 : camelCase, PascalCase, snake_case, kebab-case
 * - 접두사, 접미사 : data-* , *Container, *Type, ... 동사로 시작하면 함수, _, $, ...
 * 
 * - 저장소, 폴더, 파일, 함수 ,변수, 상수, 깃 브랜치, 컨밋 등
 * - 프로그래밍 전반적으로 이름을 짓기 위해 네이밍을 위한 규칙이나 관습을 만드는 것.
 * 
 * - 팀이나 개인의 차원에 따라 다를 수 있으며 특히 개인적인 견해와 해석에 따라 다를 수 있다.
 * - 기준을 설정할 때 기본적인 논리와 이유가 있어야 한다.
 * 
 * - 가장 중요한 것 : 언어의 예약어 (키워드) 와 겹치지 말 것.
 * 
 */